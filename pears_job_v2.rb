attempts = 500
votes = 0

attempts.times do |count|
  thread_count = 5
  threads = Array.new(thread_count).map { Thread.new{ voter = PearsVoters.new; voter.vote } }
  threads.each { |t| t.join }

  votes += thread_count
  puts "Total of #{votes} votes attempted for this session!"
  sleep(0.5)
end

