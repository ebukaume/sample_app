  def initialize
    @end_point = 'https://pearsbaby.ng/wp-admin/admin-ajax.php'
  end

  def vote
    RestClient::Request.new({
      method: :post,
      url: @end_point,
      payload: payload,
      headers: header}).execute do |response, request, result|
        case response.code
        when 500
          {status: false, message: "Internal Server Error!"}
        when 200
            {status: true, message: "Vote Accepted!"}
        else
          {status: false, message: "Unknown Error!"}
        end
      end
  end

  private

  def payload
    "action=vote_for_photo&photo_id=18998&nonce_id=2ddef450dc&option_id=basic"
  end

